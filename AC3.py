def eq(a, b, c):
    if a == 0:
        return "Esta equação não é de segundo grau"
    delta = (b ** 2) - (4 * a * c)
    raiz1 = (-b + delta ** 0.5) / (2 * a)
    raiz2 = (-b - delta ** 0.5) / (2 * a)
    if delta < 0:
        return "Esta equação não possui raizes reais"
    elif delta == 0:
        return raiz1
    else:
        return raiz1, raiz2 
