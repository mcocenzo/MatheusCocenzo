def aumento(salario):
    if 0 < salario <= 280:
        print("Você recebia ", salario, "reais. Após um aumento de 20%, ou "
              , salario * 0.2, "reais, você recebe agora ", salario * 1.2, "reais")
    elif 280 < salario <= 700:
        print("Você recebia ", salario, "reais. Após um aumento de 15%, ou "
              , salario * 0.15, "reais, você recebe agora ", salario * 1.15, "reais")
    elif 700 < salario <= 1500:
        print("Você recebia ", salario, "reais. Após um aumento de 10%, ou "
              , salario * 0.1, "reais, você recebe agora ", salario * 1.1, "reais")
    else:
        print("Você recebia ", salario, "reais. Após um aumento de 5%, ou "
              , salario * 0.05, "reais, você recebe agora ", salario * 1.05, "reais")
         
